package Modelo;

/**
 *
 * @author LENOVO
 */
public class Cliente {
    
    private String usuario;
    private String clave;
    private int saldo;

    public Cliente() {
    }

    public Cliente(String usuario, String clave, int saldo) {
        this.usuario = usuario;
        this.clave = clave;
        this.saldo = saldo;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getClave() {
        return clave;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }

    public int getSaldo() {
        return saldo;
    }

    public void setSaldo(int saldo) {
        this.saldo = saldo;
    }
    
    
     
    
    
}
